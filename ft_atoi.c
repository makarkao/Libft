#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	s;
	int	x;

	i = 0;
	s = 1;
	x = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		x = (str[i] - '0') + (x * 10);
		i++;
	}
	return (x * s);
}
